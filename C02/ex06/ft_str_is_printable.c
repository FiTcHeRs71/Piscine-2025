#include <unistd.h>

int	ft_str_is_printable(char *str)
{
	int	a;
	int	c;

	a = 0;
	while (str[a] != '\0')
	{
		c = *(str + a);
		if (c <= 30 || c >= 127)
		{
			return (0);
		}
		a++;
	}
	return (1);
}
