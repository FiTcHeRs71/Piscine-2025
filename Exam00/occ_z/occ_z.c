#include <unistd.h>

int	occ_z(char *str)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (str[i])
	{
		if(str[i] == 'Z')
		{
			j++;
		}
	i++;
	}
	return(j);
}
