#include <unistd.h>

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		write(1, &str[i], 1);
		i++;
	}
	write(1, "\n", 1);
}

int	main(int argc, char **argv)
{
	int	i;

	if(argc < 2)
	{
		write(1, "\n", 1);
		return(0);
	}
	i = argc - 1;
	ft_putstr(argv[i]);
	return (0);
}
